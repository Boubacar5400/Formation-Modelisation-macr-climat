function [lhs, rhs] = static_resid(y, x, params)
T = NaN(9, 1);
lhs = NaN(15, 1);
rhs = NaN(15, 1);
T(1) = (1-y(5))^(1-params(5));
T(2) = y(1)^params(5);
T(3) = T(1)*T(2);
T(4) = params(5)*T(3)^(-params(3));
T(5) = y(1)^(params(5)-1);
T(6) = params(7)^(params(5)*(1-params(3))-1);
T(7) = T(6)*y(8)*params(2)/(1+y(13));
T(8) = x(1)*(y(3)/params(7))^params(1);
T(9) = y(5)^(1-params(1));
lhs(1) = y(8);
rhs(1) = T(1)*T(4)*T(5);
lhs(2) = y(8)*(1+x(3))/(1+x(2));
rhs(2) = T(7)*(y(6)*(1-y(14))+(1-params(4))*(1+y(15)));
lhs(3) = y(7);
rhs(3) = T(8)*T(9);
lhs(4) = y(7);
rhs(4) = y(1)+y(2)+y(9);
lhs(5) = y(2);
rhs(5) = y(3)-y(3)*(1-params(4))/params(7);
lhs(6) = y(6)/(1-x(7));
rhs(6) = params(7)*y(7)*params(1)/y(3);
lhs(7) = y(4)*(1+x(5))/(1-x(7));
rhs(7) = y(7)*(1-params(1))/y(5);
lhs(8) = y(4);
rhs(8) = (1+x(2))*y(1)*(1-params(5))/params(5)/(1-y(5))/(1-x(4));
lhs(9) = y(9);
rhs(9) = y(1)*x(2)+x(3)*y(2)+y(5)*y(4)*(x(5)+x(4))+y(3)*y(6)*x(6)/params(7)+y(7)*x(7)+x(8);
lhs(10) = y(10);
rhs(10) = y(7)*y(12);
lhs(11) = y(11);
rhs(11) = y(7)*y(12)+y(11)*(1-params(8))/params(7);
lhs(12) = log(y(12));
rhs(12) = (1-params(9))*log(params(10))+log(y(12))*params(9);
lhs(13) = y(13);
rhs(13) = x(2);
lhs(14) = y(14);
rhs(14) = x(6);
lhs(15) = y(15);
rhs(15) = x(3);
end
