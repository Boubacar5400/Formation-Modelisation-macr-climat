function [lhs, rhs] = static_resid(y, x, params)
T = NaN(9, 1);
lhs = NaN(13, 1);
rhs = NaN(13, 1);
T(1) = (1-y(5))^(1-params(5));
T(2) = y(1)^params(5);
T(3) = T(1)*T(2);
T(4) = params(5)*T(3)^(-params(3));
T(5) = y(1)^(params(5)-1);
T(6) = params(7)^(params(5)*(1-params(3))-1);
T(7) = y(9)*params(2)*T(6);
T(8) = x(1)*(y(3)/params(7))^params(1);
T(9) = y(5)^(1-params(1));
lhs(1) = y(9);
rhs(1) = T(1)*T(4)*T(5);
lhs(2) = y(9);
rhs(2) = T(7)*(1+y(6)-params(4));
lhs(3) = y(4);
rhs(3) = y(1)*(1-params(5))/params(5)/(1-y(5));
lhs(4) = y(8);
rhs(4) = T(8)*T(9);
lhs(5) = y(13);
rhs(5) = params(10)*exp((-params(11))*(y(11)-params(12)));
lhs(6) = y(7);
rhs(6) = y(8)*y(13);
lhs(7) = y(7);
rhs(7) = y(1)+y(2);
lhs(8) = y(2);
rhs(8) = y(3)-y(3)*(1-params(4))/params(7);
lhs(9) = y(6);
rhs(9) = params(7)*params(1)*y(7)/y(3);
lhs(10) = y(4);
rhs(10) = (1-params(1))*y(7)/y(5);
lhs(11) = y(12);
rhs(11) = (1-params(9))*params(13)+y(12)*params(9);
lhs(12) = y(10);
rhs(12) = y(7)*y(12);
lhs(13) = y(11);
rhs(13) = y(10)+y(11)*(1-params(8))/params(7);
end
